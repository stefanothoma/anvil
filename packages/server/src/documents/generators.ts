// ─── Context Files ────────────────────────────────────────────────────────────

/**
 * Generates context files in all major AI tool formats from the same
 * project data. One source, four outputs.
 */
export function generateContextFiles(
  project: Project,
  existingContent?: Partial<GeneratedContextFiles>
): GeneratedContextFiles {
  const constraints = formatConstraints(project.constraints);

  const coreContext = `## Project: ${project.name}

${project.description}

## Developer
${project.developerName}${project.developerContext ? ` — ${project.developerContext}` : ""}

## Architecture
${project.architecture || "Not yet defined."}

## Tech Stack
${project.stack || "Not yet defined."}

## Hard Constraints
${constraints}

## Test Command
\`${project.testCommand}\`

## Development Workflow
Always propose before implementing. Run tests after every change. Never move forward on a failing test.`;

  const claudeMd = mergeWithExisting(
    `# ${project.name} — Claude Instructions

${coreContext}

## Environment
${project.environment || "Not yet defined."}

## Current State
${project.currentState || "Initial setup. No implementation has begun."}
`,
    existingContent?.claudeMd
  );

  const agentsMd = mergeWithExisting(
    `# ${project.name}

${coreContext}
`,
    existingContent?.agentsMd
  );

  const cursorRules = mergeWithExisting(
    `# ${project.name} Cursor Rules

${project.description}

## Stack
${project.stack || "Not yet defined."}

## Constraints
${constraints}

## Workflow
Propose before implementing. Test after every change. No forward progress on failing tests.

## Test Command
${project.testCommand}
`,
    existingContent?.cursorRules
  );

  const copilotInstructions = mergeWithExisting(
    `# Copilot Instructions — ${project.name}

${project.description}

## Architecture
${project.architecture || "Not yet defined."}

## Stack
${project.stack || "Not yet defined."}

## Rules
${constraints}
`,
    existingContent?.copilotInstructions
  );

  return { claudeMd, agentsMd, cursorRules, copilotInstructions };
}

// ─── Merge helper ─────────────────────────────────────────────────────────────

/**
 * Merges Anvil-generated content with existing developer-written content.
 * Existing content appended under a separator so neither is lost.
 */
function mergeWithExisting(generated: string, existing?: string): string {
  if (!existing || !existing.trim()) return generated;
  return `${generated}
---

## Existing Project Context (imported)

${existing.trim()}
`;
}
